#import "SSViewLicenseViewController.h"

@interface SSViewLicenseViewController ()

@property (nonatomic) License license;
@property (strong, nonatomic, nonnull) SSTextView *textview;

@end

@implementation SSViewLicenseViewController

#pragma mark - Initializer

- (instancetype)initWithLicense:(License)license
{
    self = [super init];
    
    if (self)
    {
        self.license = license;
        self.title = @"License";
        UIBarButtonItem *done = [[UIBarButtonItem alloc] initWithBarButtonSystemItem:UIBarButtonSystemItemDone target:self action:@selector(dismissController)];
        self.navigationItem.rightBarButtonItem = done;
        
        self.textview = [[SSTextView alloc] initWithTextStyle:UIFontTextStyleCaption1];
        self.textview.frame = self.view.bounds;
        self.textview.editable = NO;
        self.textview.scrollEnabled = YES;
        self.textview.dataDetectorTypes = UIDataDetectorTypeLink;
        [self.view addSubview:self.textview];
        [self.textview mas_makeConstraints:^(MASConstraintMaker *make) {
            make.top.equalTo(self.view.mas_safeAreaLayoutGuideTop).with.offset(SSTopElementMargin);
            make.left.equalTo(self.view.mas_leftMargin);
            make.right.equalTo(self.view.mas_rightMargin);
            make.bottom.equalTo(self.view.mas_safeAreaLayoutGuideBottom).with.offset(SSBottomBigElementMargin);
        }];
        
        self.view.backgroundColor = [UIColor systemBackgroundColor];
    }
    
    return self;
}

- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    self.textview.text = [self markDownTextForLicense:self.license];
}

#pragma mark - Lazy Text Generation

- (NSString *)markDownTextForLicense:(License)license
{
    switch (license)
    {
        case LicenseFMDB:
            return [self fmdbText];
            break;
        case LicenseMasonry:
            return [self masonryText];
            break;
        case LicenseIGListKit:
            return [self igListKitText];
            break;
        default:
            return @"";
            break;
    }
}

